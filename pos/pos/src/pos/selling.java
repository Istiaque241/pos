    private void order(){
        String name=txtProduct.getText();
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con1=DriverManager.getConnection("jdbc:mysql://localhost/pos","root","");
                insert=con1.prepareStatement("select * from product where name=?");
                insert.setString(1,name);
                rs=insert.executeQuery();
                while(rs.next()){
                    int stock;
                    stock=rs.getInt("stock");
                    int quantity=Integer.parseInt(txtQuantity.getText());
                    int sell= Integer.parseInt(txtSell.getText());
                    int discount=Integer.parseInt(txtDiscount.getText());
                    int total=(sell-discount)*quantity;
                    if(quantity>=stock){
                        JOptionPane.showMessageDialog(this,"Not Enough");
                    }
                    else{
                        model= (DefaultTableModel)jTable1.getModel();
                        model.addRow(new Object[]{
                            txtProduct.getText(),
                            txtUnit.getText(),
                            txtQuantity.getText(),
                            txtSell.getText(),
                            txtDiscount.getText(),
                            total,
                        });
                        
                        int sum=0;
                        for(int i=0; i<jTable1.getRowCount();i++){
                            sum=sum+Integer.parseInt(jTable1.getValueAt(i, 5).toString());
                        }
                        txtSubtotal.setText(Integer.toString(sum));
                        
                        txtProduct.setText("");
                        txtStock.setText("");
                        txtUnit.setText("");
                        txtQuantity.setText("");
                        txtSell.setText("");
                        txtDiscount.setText("");
                        txtProduct.requestFocus();
                    }
                }
                
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(selling.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
