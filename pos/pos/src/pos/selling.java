    private void sales(){
        String phone=txtPhone.getText();
        String subtotal=txtSubtotal.getText();
        int last=0;
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con1=DriverManager.getConnection("jdbc:mysql://localhost/pos","root","");
            String query="insert into customer (PHONE,SUBTOTAL) values (?,?)";
            insert=con1.prepareStatement(query,Statement.RETURN_GENERATED_KEYS);
            insert.setString(1,phone);
            insert.setString(2,subtotal);
            insert.executeUpdate();
            ResultSet generatedKeyResult=insert.getGeneratedKeys();
            if(generatedKeyResult.next()){
                last=generatedKeyResult.getInt(1);
            }
            JOptionPane.showMessageDialog(this, "Inserted");
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(product.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
    
